    @Test
    public void testBookSeats() {
        int[] booked = {5, 6};
        cinema = Main.bookSeats(0, 5, booked, cinema);
        assertEquals(0, cinema[0][4][4]);
        assertEquals(1, cinema[0][4][5]);

        booked = new int[]{2, 3};
        cinema = Main.bookSeats(0, 6, booked, cinema);
        assertEquals(0, cinema[0][5][1]);
        assertEquals(1, cinema[0][5][2]);
    }

    @Test
    public void testPrintSeatingArrangement() {
        int hallNumber = 0;
        int[][][] cinema = new int[1][1][1];

        ByteArrayOutputStream forTest = new ByteArrayOutputStream();
        System.setOut(new PrintStream(forTest));

        Main.printSeatingArrangement(hallNumber, cinema);
        System.setOut(System.out);

        assertTrue(hallNumber >= 0);

        assertTrue(cinema instanceof int[][][]);
        assertTrue(cinema.length > 0);

        assertTrue(cinema[0] instanceof int[][]);
        assertTrue(cinema[0].length > 0);

        assertTrue(cinema[0][0] instanceof int[]);
    }
