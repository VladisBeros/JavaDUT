    public static String checkAvailability(int[][][] cinema, int screen, int numSeats) {
        String request = null;
        int rows = cinema[screen].length;
        int seatsPerRow = cinema[screen][0].length;

        for (int a = 0; a < rows; a++) {
            for (int b = 0; b < seatsPerRow - numSeats; b++) {
                boolean available = true;
                for (int c = 0; c < numSeats; c++) {
                    if (cinema[screen][a][b + c] == 1) {
                        available = false;
                        break;
                    }
                }

                if (available) {
                    request = "Доступнi мiсця у залi " + (screen + 1) + ": ряд " + (a + 1) + ", мiсця " + (b + 1) + " - " + (b + numSeats);
                    return request.toString();
                }
            }
        }

        request = "Усi мiсця у залi зайнятi.";
        return request.toString();
    }

    public static int[][][] bookSeats(int hallNumber, int row, int[] seats, int[][][] cinema) {
        for (int b = 0; b < row; b++) {
            for (int c = 0; c < seats.length; c++) {
                if (cinema[hallNumber][b][seats[c]] == 0 && (b + 1) == row) {
                    cinema[hallNumber][b][seats[c]] = 1;
                }
            }
        }
        return cinema;
    }

